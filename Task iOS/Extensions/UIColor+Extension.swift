import UIKit

extension UIColor {
    static var spotifyBlack: UIColor {
        return UIColor(red: 25 / 255.0, green: 20 / 255.0, blue: 20 / 255.0, alpha: 1)
    }
    
    static var spotifyGreen: UIColor {
        return UIColor(red: 30 / 255.0, green: 215 / 255.0, blue: 96 / 255.0, alpha: 1)
    }
}
